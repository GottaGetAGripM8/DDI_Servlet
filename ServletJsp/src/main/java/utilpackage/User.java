package utilpackage;

public class User {
	
	private String name;
	private int age;
	private String course;
	
	public String getName(){
		return name;
	}
	
	public void setName(String n){
		name = n;
	}
	
	//---------------------------------//
	
	public int getAge(){
		return age;
	}
	
	public void setAge(int a){
		age = a;
	}

	//---------------------------------//
	
	public String getCourse(){
		return course;
	}
	
	public void setCourse(String c){
		course = c;
	}
}
